package Assignment;

public class Employee {
	
	  int id;
	    String name;
	    double baseSalary;
	    double experienceYears;
	    double yearEndRating;

	    Employee(int id, String name, double baseSalary, double experienceYears, double yearEndRating) 
	    {
	        this.id = id;
	        this.name = name;
	        this.baseSalary = baseSalary;
	        this.experienceYears = experienceYears;
	        this.yearEndRating = yearEndRating;
	    }

}
